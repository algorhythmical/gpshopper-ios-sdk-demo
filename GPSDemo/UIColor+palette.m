#import "UIColor+palette.h"

@implementation UIColor (palette)

+ (UIColor *)GPDBlueColor
{
    return [UIColor colorWithRed:71.f/255.f green:158.f/255.f blue:215.f/255.f alpha:1.0];
}

+ (UIColor *)GPDDarkBlueColor
{
    return [UIColor colorWithRed:40.0/255.0 green:85.0/255.0 blue:120.0/255.0 alpha:1.0];
}

@end
